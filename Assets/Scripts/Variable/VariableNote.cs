using UnityEngine;

public class VariableNote : MonoBehaviour
{
    // Start is called once before the first execution of Update after the MonoBehaviour is created
    void Start()
    {
        //[1] 변수 만들기
        int i; //i 이름으로 정수형 변수를 만든다

        //[2] 변에 값을 저장하기
        i = 1234;

        Debug.Log(i);
    }


}
